const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  fname: {
    type: String,
    required: true,
  },
  lname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  userType: {
    type: String,
    required: true,
  },
});

userSchema.methods.remove = async function () {
  await this.model("User").deleteOne({ _id: this._id });
};

module.exports = mongoose.model("User", userSchema);
